    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        Connection conn = getKoneksi();
        if("Add".equals(addButton.getText())) {
            addButton.setText("Save");
            refreshButton.setEnabled(false);
        } else if ("Save".equals(addButton.getText())) {
            String sqlKode = "INSERT INTO dtbakery(kode, nama_bakery, jenis_bakery, stok, harga) VALUES" +
            "('" + kodeTextField.getText() + "', " +
            " '" + nbComboBox.getSelectedItem().toString() + "', " +
            " '" + jbComboBox.getSelectedItem().toString() + "', " +
            " '" + stokTextField.getText() + "', " +
            " '" + hargaTextField.getText() + "');";

            try {
                PreparedStatement ps = (PreparedStatement) conn.prepareStatement(sqlKode);
                ps.executeUpdate();
                ps.close();

                backIntoDefaultState();
            } catch(SQLException e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan " + e.getMessage());
            }

            addButton.setText("Add");
            refreshButton.setEnabled(true);
        } else if("Update".equals(addButton.getText())) {
            String sqlKode = "UPDATE dtbakery SET nama_bakery = '" + nbComboBox.getSelectedItem().toString() + "'," +
            " jenis_bakery = '" + jbComboBox.getSelectedItem().toString() +
            " WHERE kode = '" + kodeTextField.getText() + "';";

            try {
                PreparedStatement ps = (PreparedStatement) conn.prepareStatement(sqlKode);
                ps.executeUpdate();
                ps.close();

                backIntoDefaultState();
            } catch(SQLException e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan " + e.getMessage());
            }

            addButton.setText("Add");
            refreshButton.setEnabled(true);
        }

    }//GEN-LAST:event_addButtonActionPerformed

