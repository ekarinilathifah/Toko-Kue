    public static Connection getKoneksi() {
        if(koneksi == null) {
            try {
                String url = "jdbc:mysql://localhost:3306/bakery";
                String user = "root";
                String password ="";
                
                DriverManager.registerDriver(new com.mysql.cj.jdbc.Driver());
                koneksi = DriverManager.getConnection(url, user, password);
            }catch (SQLException e) {
                System.out.println("Koneksi Gagal");
            }
        }
        return koneksi;
    }
    
    private void ambilDataTable() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection conn = getKoneksi();
            Statement st = conn.createStatement();
            String sql = "SELECT * FROM dtbakery";
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {
                Object[] o = new Object[5];
                o[0] = rs.getString("kode");
                o[1] = rs.getString("nama_bakery");
                o[2] = rs.getString("jenis_bakery");
                o[3] = rs.getString("stok");
                o[4] = rs.getString("harga");
               
                model.addRow(o);
            }
            rs.close();
            st.close();
            ambilTableKlik();
        } catch(SQLException e) {
            System.out.println("Terjadi kesalahan " + e.getMessage());
        }
    }
    
     private void ambilTableKlik() {
        int i = tabel_bakery.getSelectedRow();
        if (i == -1) {
            return;
        }
        String id = (String) model.getValueAt(i, 1);
        kodeTextField.setText(id);
        String nama = (String) model.getValueAt(i, 2);
        nbComboBox.getSelectedItem().toString();
        String jenis_bakery = (String) model.getValueAt(i, 3);
        jbComboBox.getSelectedItem().toString();
        String stok = (String) model.getValueAt(i, 4);
        stokTextField.setText(stok);
        String harga = (String) model.getValueAt(i, 5);
        hargaTextField.setText(harga);
        
        addButton.setEnabled(false);
    }
     
